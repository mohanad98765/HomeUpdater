export type ProviderId = "local" | "cloud";

export interface SolveRequest {
  /** سؤال المستخدم / وصف المشكلة. */
  question: string;
  /** سياق من صفحة الأمان (المصنّع + قائمة الثغرات) — يُحقَن في التعليمة. */
  context?: string;
  /** بثّ تدريجيّ للاستجابة حتى تبدو الواجهة فوريّة (نفس إحساس السحابيّ). */
  onToken?: (chunk: string) => void;
  /** تقدّم تحميل النموذج المحليّ لأوّل مرّة (0..1) — للمحليّ فقط. */
  onProgress?: (fraction: number, text: string) => void;
  /** لإلغاء الطلب. */
  signal?: AbortSignal;
}

export interface AIProvider {
  readonly id: ProviderId;
  /** هل المزوّد جاهزٌ للعمل على هذا الجهاز بالذات؟ (مفتاح مضبوط / WebGPU مدعوم). */
  isAvailable(): Promise<boolean>;
  /** ينفّذ الطلب ويعيد الإجابة الكاملة (ويبثّ عبر onToken أثناء ذلك). */
  solve(req: SolveRequest): Promise<string>;
  /** تحرير الموارد — يُستدعى عند التبديل، لعزل ذاكرة المحليّ عن السحابيّ. */
  dispose?(): Promise<void>;
}
